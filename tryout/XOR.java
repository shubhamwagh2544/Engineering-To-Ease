package com.engineering.tryout;

public class XOR {
    public static void main(String[] args) {
        //1 : XOR of two same numbers is always zero
        System.out.println(1^1);        //0
        System.out.println(1^1^1);      //0^1 = 1
        System.out.println(1^1^1^1);    //0^0 = 0
    }
}
